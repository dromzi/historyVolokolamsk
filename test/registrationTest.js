describe('регистрация', () => {

    const register = (имя, фамилия, логин, пароль, парольПодтверждение) => {

        // Проверка на нецензурные слова
        const нецензурныеСлова = ['слово1', 'слово2'];
        
        if (содержитНецензурноеСлово(имя) 
          || содержитНецензурноеСлово(фамилия)
          || содержитНецензурноеСлово(логин)) {
          return false;
        }
      
        // Проверка длины
        if (имя.length < 5 
          || фамилия.length < 5
          || логин.length < 5) {
          return false;
        }
      
        if (пароль.length < 6) {
          return false; 
        }
      
        // Проверка совпадения паролей
        if (пароль !== парольПодтверждение) {
          return false;
        }
      
        // Если все ОК
        return true;
      
      };
      
      function содержитНецензурноеСлово(строка) {
        return нецензурныеСлова.includes(строка); 
      }
  
    it('должна вернуть true при валидных данных', () => {
      const result = register('Иван', 'Иванов', 'ivan123', 'сложныйпароль123', 'сложныйпароль123');
      expect(result).toBe(true);
    });
  
    it('должна вернуть false если имя содержит нецензурные слова', () => {
      const result = register('Иван###', 'Иванов', 'ivan123', 'сложныйпароль123', 'сложныйпароль123');
      expect(result).toBe(false);
    });
  
    it('должна вернуть false если фамилия содержит нецензурные слова', () => {
      const result = register('Иван', 'Иванов$$$', 'ivan123', 'сложныйпароль123', 'сложныйпароль123');
      expect(result).toBe(false);
    });
  
    it('должна вернуть false если логин содержит нецензурные слова', () => {
      const result = register('Иван', 'Иванов', 'iva@#123', 'сложныйпароль123', 'сложныйпароль123');
      expect(result).toBe(false);
    });
  
    it('должна вернуть false если имя меньше 5 символов', () => {
      const result = register('Ива', 'Иванов', 'ivan123', 'сложныйпароль123', 'сложныйпароль123');
      expect(result).toBe(false);
    });
  
    it('должна вернуть false если фамилия меньше 5 символов', () => {
      const result = register('Иван', 'Ива', 'ivan123', 'сложныйпароль123', 'сложныйпароль123');
      expect(result).toBe(false);
    });
  
    it('должна вернуть false если логин меньше 5 символов', () => {
      const result = register('Иван', 'Иванов', 'iva1', 'сложныйпароль123', 'сложныйпароль123');
      expect(result).toBe(false);
    });
  
    it('должна вернуть false если пароль меньше 6 символов', () => {
      const result = register('Иван', 'Иванов', 'ivan123', 'пар12', 'пар12');
      expect(result).toBe(false);
    });
  
    it('должна вернуть false если пароли не совпадают', () => {
      const result = register('Иван', 'Иванов', 'ivan123', 'сложныйпароль123', 'пароль456');
      expect(result).toBe(false);
    });
  
  });